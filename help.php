	<p><h1>Carpooling: líderes mundiales en viajes en coche compartido.</p></h1>
	<font size="4px">
	Presente en Argentina desde Agosto del 2018, Carpolling.com.ar es la mayor red social de viajes de larga distancia en coche compartido.<br /> 
	La red social pone en contacto a personas que quieren realizar un trayecto común y coinciden para hacerlo el mismo día.<br />
	Los usuarios comparten los gastos del viaje sin obtener beneficio.<br /> 
	Para ello, Carpooling recomienda en la plataforma una aportación por usuario apropiada para la compartición de gastos inherentes a la conducción (gasolina, peaje, mantenimiento, seguros, impuestos, etc.) y no limita la aportación máxima que pueden solicitar los conductores de tal manera que no se superen estos gastos.<br />
	</font>
	<p><h2>¿Cómo Funciona?</p></h2>
	<h3>1- Busca tu viaje</h3>
	<font size="4px">
	<b>A-</b> Indica la ciudad de salida y de llegada, así como la fecha en la que vas a viajar.<br /> 
	A continuación, elige un viaje que te venga bien. Si tienes cualquier duda, envía un mensaje al conductor antes de realizar la reserva.<br />  
	Consulta los perfiles de los conductores para conocer la opinión de otros pasajeros que han viajado con ellos.
	</font><p></p>
	<h3>2- Postulate</h3>
	<font size="4px">
	<b>A-</b> Pulsa para postularse y al finalizar el viaje abona tu aportación.<br />
	Una vez que el conductor haya confirmado tu plaza ya podrás prepararte para viajar. <br />
	Si el conductor cancela el viaje, se le restará reputación al conductor, lo mismo si unas vez aceptado tú te das de baja del viaje.
	</font><p></p>
	<h3>3- Viaje acompañado</h3>
	<font size="4px">
	<b>A-</b> ¡Disfruta del viaje y no te olvides de dejar una opinión! <br />
	<b>B-</b> Si publicas una opinión, tendrás más posibilidades de que el conductor deje una sobre ti.
	</font><p></p>
	<p><h2>¿Cómo me registro?</p></h2>
	<font size="4px">
	Registrarse en Carpooling es solo cuestión de segundos. <br /> 
	Para ello, puedes usar tu cuenta de tu correo electrónico, solo tendrás que introducir tu nombre,  apellido, clave y tu año de nacimiento.  <br /> 
	Accediendo desde la página de inicio, clica en ‘Registrarse’, y a partir de ahí sigue las instrucciones.
	</font>
	<p><h2>¿Cómo inicio sesión?</p></h2>
	<font size="4px">
	¡Es muy fácil! Haz clic en ‘Iniciar sesión’ y después introduce tu datos de inicio de sesión, ¡y ya estás dentro!
	</font>
	<p><h2>¿Qué ocurre si un pasajero cancela el viaje antes de la salida o si no se presenta?</p></h2>
	<font size="4px">
	Si las cancelaciones de última hora te parecen situaciones muy molestas, que un pasajero no se presente también es algo frustrante. <br />
	Aunque se trata de algo poco frecuente, si esto ocurre puedes informarnos accediendo a la sección de contacto enviando un email y atenderemos tu caso.<br />
	Recuerda que en cualquiera de estas dos situaciones podrás dar una opinión sobre el conductor y una mala reputación, para que otros usuarios sepan de esta situación.
	</font>
	<p><h2>El pasajero cancela con más de 24 horas de antelación</p></h2>
	<font size="4px">
	El pasajero recibirá una reputación negativa, no importa cuando se dé de baja del viaje.
	</font>
	<p><h2>El pasajero no se presenta</p></h2>
	<font size="4px">
	Una vez que nos lo notifiques, comenzaremos a trabajar en tu reclamación. <br />
	Mientras resolvemos esta situación podrás darle al pasajero una reputación negativa y dejar un comentario para que próximos conductores sepan de esta situación.
	</font>
	<p><h2>¿Qué ocurre si el viaje no transcurre tal y como se había planificado?</p></h2>
	<font size="4px">
	Si el viaje no tiene lugar tal y como se había planificado por cualquier motivo, te rogamos que nos informes de ello lo antes posible a través de la página de contacto.<br />
	Solo tienes que buscar tu viaje e indicar que no lo has hecho. <br />
	Elige el motivo y proporciónanos cualquier información que debamos conocer (no te preocupes, no la compartiremos con nadie). <br />
	A continuación, contactaremos con el conductor.
